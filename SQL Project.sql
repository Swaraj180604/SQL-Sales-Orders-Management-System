-- Get all agents:

SELECT * FROM AGENTS;


-- Get all customers from 'London':

SELECT * FROM CUSTOMER WHERE CUST_CITY = 'London';


-- Get the total number of orders:

SELECT COUNT(*) FROM ORDERS;


-- Get all customers who owe more than ₹10,000:

SELECT CUST_NAME, OUTSTANDING_AMT
FROM CUSTOMER
WHERE OUTSTANDING_AMT > 10000;


-- Get the details of orders made by customer 'Micheal':

SELECT * FROM ORDERS
WHERE CUST_CODE = 'C00001';

-- Intermediate Queries:

-- Get the total commission paid to each agent:

SELECT AGENT_CODE, SUM(COMMISSION) AS TOTAL_COMMISSION
FROM AGENTS
GROUP BY AGENT_CODE;


-- Get the average opening amount for customers from 'Bangalore':

SELECT AVG(OPENING_AMT) AS AVERAGE_OPENING_AMT
FROM CUSTOMER
WHERE CUST_CITY = 'Bangalore';


-- Find the top 3 customers who have received the most amount:

SELECT CUST_NAME, RECEIVE_AMT
FROM CUSTOMER
ORDER BY RECEIVE_AMT DESC
LIMIT 3;


-- Get the total amount of orders placed by customers in 'Mumbai':

SELECT SUM(ORD_AMOUNT) AS TOTAL_ORDER_AMOUNT
FROM ORDERS
WHERE CUST_CODE IN (SELECT CUST_CODE FROM CUSTOMER WHERE CUST_CITY = 'Mumbai');


-- Get the number of orders placed by each customer (join ORDERS and CUSTOMER):

SELECT CUST_NAME, COUNT(ORD_NUM) AS ORDER_COUNT
FROM CUSTOMER
JOIN ORDERS ON CUSTOMER.CUST_CODE = ORDERS.CUST_CODE
GROUP BY CUST_NAME;

-- Advanced Queries:

-- Get the agents who have handled the most orders, along with the number of orders:

SELECT AGENT_CODE, COUNT(ORD_NUM) AS ORDER_COUNT
FROM ORDERS
GROUP BY AGENT_CODE
ORDER BY ORDER_COUNT DESC
LIMIT 5;


-- Get the total outstanding amount for each agent (based on the customers they handle):

SELECT AGENT_CODE, SUM(OUTSTANDING_AMT) AS TOTAL_OUTSTANDING
FROM CUSTOMER
GROUP BY AGENT_CODE
ORDER BY TOTAL_OUTSTANDING DESC;


-- Get the total order amount and advance amount for each agent:

SELECT AGENT_CODE, SUM(ORD_AMOUNT) AS TOTAL_ORDER_AMOUNT, SUM(ADVANCE_AMOUNT) AS TOTAL_ADVANCE
FROM ORDERS
GROUP BY AGENT_CODE;


-- Find customers who have both received and paid more than ₹5,000:

SELECT CUST_NAME
FROM CUSTOMER
WHERE RECEIVE_AMT > 5000 AND PAYMENT_AMT > 5000;


-- Get the total amount of orders for each city (grouped by customer city):

SELECT CUST_CITY, SUM(ORD_AMOUNT) AS TOTAL_ORDER_AMOUNT
FROM ORDERS
JOIN CUSTOMER ON ORDERS.CUST_CODE = CUSTOMER.CUST_CODE
GROUP BY CUST_CITY;

-- Join Queries:

-- Get the names of customers and their corresponding agent names:

SELECT CUSTOMER.CUST_NAME, AGENTS.AGENT_NAME
FROM CUSTOMER
JOIN AGENTS ON CUSTOMER.AGENT_CODE = AGENTS.AGENT_CODE;


-- Get a list of orders with customer names, agent names, and order amounts:

SELECT ORDERS.ORD_NUM, CUSTOMER.CUST_NAME, AGENTS.AGENT_NAME, ORDERS.ORD_AMOUNT
FROM ORDERS
JOIN CUSTOMER ON ORDERS.CUST_CODE = CUSTOMER.CUST_CODE
JOIN AGENTS ON ORDERS.AGENT_CODE = AGENTS.AGENT_CODE;


-- Get the total orders and total advance amount for each agent along with customer names:

SELECT AGENTS.AGENT_NAME, CUSTOMER.CUST_NAME, SUM(ORDERS.ORD_AMOUNT) AS TOTAL_ORDERS, SUM(ORDERS.ADVANCE_AMOUNT) AS TOTAL_ADVANCE
FROM ORDERS
JOIN CUSTOMER ON ORDERS.CUST_CODE = CUSTOMER.CUST_CODE
JOIN AGENTS ON ORDERS.AGENT_CODE = AGENTS.AGENT_CODE
GROUP BY AGENTS.AGENT_NAME, CUSTOMER.CUST_NAME;


-- Get the total outstanding amount for customers handled by each agent:

SELECT AGENTS.AGENT_NAME, SUM(CUSTOMER.OUTSTANDING_AMT) AS TOTAL_OUTSTANDING
FROM CUSTOMER
JOIN AGENTS ON CUSTOMER.AGENT_CODE = AGENTS.AGENT_CODE
GROUP BY AGENTS.AGENT_NAME;


-- Find the customers whose outstanding amount exceeds their opening amount (using INNER JOIN):

SELECT CUSTOMER.CUST_NAME, CUSTOMER.OUTSTANDING_AMT, CUSTOMER.OPENING_AMT
FROM CUSTOMER
WHERE CUSTOMER.OUTSTANDING_AMT > CUSTOMER.OPENING_AMT;

-- Aggregate Functions & Grouping:

-- Get the average commission of all agents:

SELECT AVG(COMMISSION) AS AVERAGE_COMMISSION
FROM AGENTS;


-- Get the maximum and minimum outstanding amounts of customers by city:

SELECT CUST_CITY, MAX(OUTSTANDING_AMT) AS MAX_OUTSTANDING, MIN(OUTSTANDING_AMT) AS MIN_OUTSTANDING
FROM CUSTOMER
GROUP BY CUST_CITY;


-- Find the top 5 cities with the highest total outstanding amounts:

SELECT CUST_CITY, SUM(OUTSTANDING_AMT) AS TOTAL_OUTSTANDING
FROM CUSTOMER
GROUP BY CUST_CITY
ORDER BY TOTAL_OUTSTANDING DESC
LIMIT 5;


-- Get the number of customers in each city:

SELECT CUST_CITY, COUNT(*) AS CUSTOMER_COUNT
FROM CUSTOMER
GROUP BY CUST_CITY;


-- Get the average order amount per city (join with CUSTOMER):

SELECT CUSTOMER.CUST_CITY, AVG(ORDERS.ORD_AMOUNT) AS AVERAGE_ORDER_AMOUNT
FROM ORDERS
JOIN CUSTOMER ON ORDERS.CUST_CODE = CUSTOMER.CUST_CODE
GROUP BY CUSTOMER.CUST_CITY;
